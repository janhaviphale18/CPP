#include <iostream>
using namespace std;
int main()
{
    // This is a single line comment
    cout << "This is a single line comment" << endl;
    cout << "Single line comments are used for short explanations" << endl;
    /* This is a multiline comment
    Multiline comments are used for longer explanations
    Multiline comments are used to comment out blocks of code */
    cout << "This is a multiline comment" << endl;
    cout << "Multiline comments can span multiple lines" << endl;
    return 0;
}