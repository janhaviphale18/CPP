#include <iostream>
using namespace std;
int main()
{
    char letter = 'A';
    cout << "Character: " << letter << endl;
    cout << "ASCII Value of A: " << (int)letter << endl;
    cout << endl;

    int asciiValue = 66;

    cout << "ASCII Number: " << asciiValue << endl;
    cout << "Character of ASCII 66: " << (char)asciiValue << endl;

    return 0;
}